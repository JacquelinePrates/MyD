package com.tt.model;

public class Conta {

	private String numeroDaConta;
	private String nomeDaAgencia;
	private String numeroDaAgencia;
	private String saldo;
	private String creditoDisponivel;
	private String tipoDaOperacao;
	public String getNumeroDaConta() {
		return numeroDaConta;
	}
	public void setNumeroDaConta(String numeroDaConta) {
		this.numeroDaConta = numeroDaConta;
	}
	public String getNomeDaAgencia() {
		return nomeDaAgencia;
	}
	public void setNomeDaAgencia(String nomeDaAgencia) {
		this.nomeDaAgencia = nomeDaAgencia;
	}
	public String getNumeroDaAgencia() {
		return numeroDaAgencia;
	}
	public void setNumeroDaAgencia(String numeroDaAgencia) {
		this.numeroDaAgencia = numeroDaAgencia;
	}
	public String getSaldo() {
		return saldo;
	}
	public void setSaldo(String saldo) {
		this.saldo = saldo;
	}
	public String getCreditoDisponivel() {
		return creditoDisponivel;
	}
	public void setCreditoDisponivel(String creditoDisponivel) {
		this.creditoDisponivel = creditoDisponivel;
	}
	public String getTipoDaOperacao() {
		return tipoDaOperacao;
	}
	public void setTipoDaOperacao(String tipoDaOperacao) {
		this.tipoDaOperacao = tipoDaOperacao;
	}
}
